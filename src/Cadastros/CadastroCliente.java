    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        if (jTextFieldCdCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "É preciso informar o código do cliente que deseja excluir!");
            jTextFieldCdCliente.grabFocus();
        } else {
            try {
                cliente.setCdCliente(Integer.parseInt(jTextFieldCdCliente.getText()));
                cliente.retornaCliente(cliente);
                if (cliente.getInAtivo().equals("INATIVO")){
                    JOptionPane.showMessageDialog(null, "Este cliente já está inativo!");
                }
                else if (cliente.parcelasAPagar(cliente)){
                    JOptionPane.showMessageDialog(null, "Este cliente possui parcelas a serem pagas, não pode ser inativado!");
                }
                else{
                    int delete = JOptionPane.showConfirmDialog(null, "Deseja realmente inativar este cliente ?",
                        "Inativar Cliente", JOptionPane.YES_OPTION);
                    if (delete == JOptionPane.YES_OPTION) {
                        cliente.excluir(cliente);
                        //JOptionPane.showMessageDialog(null, "Cliente inativado com sucesso!");
                        limpar.limparCampos(jPanelCadastro);
                    }
                }
            } 
            catch (NumberFormatException ex) {
            
            }
        }
        rotina = Rotinas.excluir;
        botoes.validaBotoes(jPanelBotoes, rotina);
        carregarUsuario();
    }//GEN-LAST:event_jBtExcluirActionPerformed

    private void jBtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGravarActionPerformed
        if (jTextFieldNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome do cliente é obrigatório!");
            jTextFieldNome.grabFocus();
        } 
        
        else if (jFormattedTextFieldCpfCNPJ.getText().equals("   .   .   -  ")
                || jFormattedTextFieldCpfCNPJ.getText().equals("  .   .   /    -  ")) {
            JOptionPane.showMessageDialog(null, "O CPF/CNPJ do cliente é obrigatório!");
            jFormattedTextFieldCpfCNPJ.grabFocus();
        } 
        
        else if (rotina == Rotinas.incluir && !doc.validaCPFCNPJ(jFormattedTextFieldCpfCNPJ.getText())) {
            JOptionPane.showMessageDialog(null, "CPF/CNPJ inválido!");
            jFormattedTextFieldCpfCNPJ.grabFocus();
        } 
        
        else if (rotina == Rotinas.alterar && jRadioButtonFisica.isSelected()
                && !doc.validaCPF(doc.removeMascara(jFormattedTextFieldCpfCNPJ.getText()))) {
            JOptionPane.showMessageDialog(null, "CPF inválido!");
            jFormattedTextFieldCpfCNPJ.grabFocus();
        } 
        
        else if (rotina == Rotinas.alterar && jRadioButtonJuridica.isSelected()
                && !doc.validaCNPJ(doc.removeMascara(jFormattedTextFieldCpfCNPJ.getText()))) {
            JOptionPane.showMessageDialog(null, "CNPJ inválido!");
            jFormattedTextFieldCpfCNPJ.grabFocus();
        } 
        
        else if ((dataAtual.comparaData(jFormattedTextFieldData.getText()) > 0) 
            ||!dataAtual.dataExiste(jFormattedTextFieldData.getText())){
            JOptionPane.showMessageDialog(null, "Por favor informe uma data válida!");
        }
        
        else if (!campos.CEPValido(jFTextFieldCEP)){
            JOptionPane.showMessageDialog(null, "Por favor preencha completamente o CEP ou mantenha-o vazio!");
        }
        
        else if (!campos.fonesValidos(jFtextFieldFoneRes, jFTextFieldFoneCom, jFTextFieldFoneCel)){
            JOptionPane.showMessageDialog(null, "Por favor preencha completamente o(s) telefone(s)"
                    + " ou mantenha-o(s) vazio(s)!");
        }
        
        else {
            gravarCliente();
            gravarEndereco();
            gravarContato();

            if (rotina == Rotinas.incluir) {
                cliente.incluir(cliente);
                //JOptionPane.showMessageDialog(null, "Cliente gravado com sucesso!");
                jTextFieldCdCliente.setText(cliente.getPessoa().getCdPessoa().toString());
            } else if (rotina == Rotinas.alterar) {
                if (jTextFieldCdCliente.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "É preciso informar o código do cliente que deseja alterar!");
                    jTextFieldCdCliente.grabFocus();
                } else {
                    try {
                        cliente.setCdCliente(Integer.parseInt(jTextFieldCdCliente.getText()));
                        cliente.alterar(cliente);
                        //JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso!");
                    } catch (NumberFormatException ex) {
                    }
                }
            }
            rotina = Rotinas.padrao;
            botoes.validaBotoes(jPanelBotoes, rotina);
            carregarUsuario();
        }
    }//GEN-LAST:event_jBtGravarActionPerformed

    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        PreencherTabela preencher = new PreencherTabela();
        preencher.FormatarJtable(jTableConsulta, new int[]{40, 180, 45, 100, 120, 180, 60, 40});

        switch (jComboBoxConsulta.getSelectedIndex()) {
            case 0:
                preencher.PreencherJtableGenerico(jTableConsulta, cliente.consultarGeral(true));
                report.setConsulta(preencher.getConsulta());
                editaBotao(preencher.Vazia());
            break;

            case 1:
                try {
                    cliente.setCdCliente(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableConsulta, cliente.consultarCdCliente(cliente, true));
                    report.setConsulta(preencher.getConsulta());
                    editaBotao(preencher.Vazia());
                } 
                catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Por favor informe um código para pesquisar!");
                    editaBotao(true);
                }
            break;

            default:
                cliente.getPessoa().setNome(jTextFieldConsulta.getText().toUpperCase());
                preencher.PreencherJtableGenerico(jTableConsulta, cliente.consultarNomeCliente(cliente, true));
                report.setConsulta(preencher.getConsulta());
                editaBotao(preencher.Vazia());
            break;
        }
    }//GEN-LAST:event_jBtPesquisarActionPerformed

