    private void gravarCliente() {
        cliente.getPessoa().setNome(jTextFieldNome.getText().toUpperCase());

        cliente.setDtCadastro(jFormattedTextFieldData.getText());
        if (jComboBoxSituacao.getSelectedIndex() == 0) {
            cliente.setInAtivo("A");
        } else {
            cliente.setInAtivo("I");
        }
        if (jRadioButtonFisica.isSelected()) {
            gravarClienteFisico();
        } else {
            gravarClienteJuridico();
        }
        cliente.getPessoa().setDtCadastro(cliente.getDtCadastro());
        cliente.getPessoa().setInAtivo(cliente.getInAtivo());
    }

    private void gravarClienteFisico() {
        cliente.getPessoa().setTpPessoa("F");
        cliente.getPessoa().getPfisica().setCPF(jFormattedTextFieldCpfCNPJ.getText());
        cliente.getPessoa().getPfisica().setRG(jTextFieldRG.getText());

        if (jComboBoxSexo.getSelectedIndex() == 0) {
            cliente.getPessoa().getPfisica().setSexo("M");
        } else {
            cliente.getPessoa().getPfisica().setSexo("F");
        }
    }

    private void gravarClienteJuridico() {
        cliente.getPessoa().setTpPessoa("J");
        cliente.getPessoa().getPjuridica().setCNPJ(jFormattedTextFieldCpfCNPJ.getText());
        cliente.getPessoa().getPjuridica().setRazao(jTextFieldRazao.getText());
    }

    private void gravarEndereco() {
        cliente.getPessoa().getEndereco().getCidade().setCdCidade(jComboBoxCidade.getSelectedIndex());
        cliente.getPessoa().getEndereco().setRuaAv(jTextFieldRua.getText().toUpperCase());
        cliente.getPessoa().getEndereco().setNrEndereco(jTextFieldNrEndereco.getText());
        cliente.getPessoa().getEndereco().setNrSequencia(1);
        cliente.getPessoa().getEndereco().setCEP(jFTextFieldCEP.getText());
        cliente.getPessoa().getEndereco().setBairro(jTextFieldBairro.getText().toUpperCase());
        cliente.getPessoa().getEndereco().setComplemento(jTextFieldComplemento.getText().toUpperCase());
    }

    private void gravarContato() {
        cliente.getPessoa().getContato().setEmail(jTextFieldEmail.getText().toUpperCase());
        cliente.getPessoa().getContato().setNrSeq(1);
        cliente.getPessoa().getContato().setNomeContato(jTextFieldNome.getText().toUpperCase());
        preencheFones();
    }

    private void preencherCampos() {
        jTextFieldCdCliente.setText(cliente.getPessoa().getCdPessoa().toString());
        jTextFieldNome.setText(cliente.getPessoa().getNome());
        jFormattedTextFieldData.setText(cliente.getDtCadastro());
        jComboBoxSituacao.setSelectedItem(cliente.getInAtivo());

        jComboBoxCidade.setSelectedItem(cliente.getPessoa().getEndereco().getCidade().getDsCidade());

        jFTextFieldCEP.setText(cliente.getPessoa().getEndereco().getCEP());
        jTextFieldNrEndereco.setText(cliente.getPessoa().getEndereco().getNrEndereco());
        jTextFieldRua.setText(cliente.getPessoa().getEndereco().getRuaAv());
        jTextFieldBairro.setText(cliente.getPessoa().getEndereco().getBairro());
        jTextFieldComplemento.setText(cliente.getPessoa().getEndereco().getComplemento());

        jTextFieldEmail.setText(cliente.getPessoa().getContato().getEmail());
        jFtextFieldFoneRes.setText(cliente.getPessoa().getContato().getFoneRes());
        jFTextFieldFoneCom.setText(cliente.getPessoa().getContato().getFoneCom());
        jFTextFieldFoneCel.setText(cliente.getPessoa().getContato().getFoneCel());

        if (cliente.getPessoa().getTpPessoa().equals("F")) {
            jRadioButtonFisica.setSelected(true);
            jRadioButtonFisicaActionPerformed(null);
            jComboBoxSexo.setSelectedItem(cliente.getPessoa().getPfisica().getSexo());
            jFormattedTextFieldCpfCNPJ.setText(cliente.getPessoa().getPfisica().getCPF());
            jTextFieldRG.setText(cliente.getPessoa().getPfisica().getRG());
        } else {
            jRadioButtonJuridica.setSelected(true);
            jRadioButtonJuridicaActionPerformed(null);
            jFormattedTextFieldCpfCNPJ.setText(cliente.getPessoa().getPjuridica().getCNPJ());
            jTextFieldRazao.setText(cliente.getPessoa().getPjuridica().getRazao());
        }
    }

    private void preencheFones() {
        if (jFtextFieldFoneRes.getText().equals("(  )-    -    ")) {
            cliente.getPessoa().getContato().setFoneRes("");
        } else {
            cliente.getPessoa().getContato().setFoneRes(jFtextFieldFoneRes.getText());
        }

        if (jFTextFieldFoneCom.getText().equals("(  )-    -    ")) {
            cliente.getPessoa().getContato().setFoneCom("");
        } else {
            cliente.getPessoa().getContato().setFoneCom(jFTextFieldFoneCom.getText());
        }

        if (jFTextFieldFoneCel.getText().equals("(  )-    -    ")) {
            cliente.getPessoa().getContato().setFoneCel("");
        } else {
            cliente.getPessoa().getContato().setFoneCel(jFTextFieldFoneCel.getText());
        }
    }
    
    private void editaBotao(boolean vazia) {
        if (vazia) {
            JOptionPane.showMessageDialog(null, "A consulta não encontrou resultados!");
            jBtRelatorio.setEnabled(false);
            jTextFieldConsulta.setText("");
            jTextFieldConsulta.grabFocus();
        } else {
            jBtRelatorio.setEnabled(valida.pRelatorio.equals("S"));
        }
    }
    
    private void carregarUsuario(){
        valida.validaNivel(this.getName());
        valida.validaBotoesUsuario(jPanelBotoes);
        jBtCidade.setEnabled(valida.validaBotaoCadastro("CADASTRAR CIDADE"));
        jBtEndereco.setEnabled(valida.validaBotaoCadastro("CADASTRAR ENDEREÇO"));
        jBtContatos.setEnabled(valida.validaBotaoCadastro("CADASTRAR CONTATO"));
    }
