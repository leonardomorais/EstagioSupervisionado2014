    private void jTextFieldCdClienteFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCdClienteFocusLost
        if (!jTextFieldCdCliente.getText().equals("")) {
            try {
                cliente.setCdCliente(Integer.parseInt(jTextFieldCdCliente.getText()));
                cliente.retornaCliente(cliente);

                if (cliente.getPessoa().getNome().equals("")) {
                    jBtCancelarActionPerformed(null);
                } else {
                    preencherCampos();
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Por favor informe um código!");
                jBtCancelarActionPerformed(null);
            }
        }
    }//GEN-LAST:event_jTextFieldCdClienteFocusLost

    private void jMenuItemCarregarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCarregarDadosActionPerformed
        int linha = jTableConsulta.getSelectedRow();

        if (linha >= 0) {
            int cd = Integer.parseInt(jTableConsulta.getValueAt(linha, 0).toString());
            cliente.setCdCliente(cd);
            cliente.retornaCliente(cliente);

            limpar.limparCampos(jPanelCadastro);
            preencherCampos();
            jTabbedPaneCliente.setSelectedIndex(0);

            rotina = Rotinas.padrao;
            botoes.validaBotoes(jPanelBotoes, rotina);
            carregarUsuario();
        }
    }//GEN-LAST:event_jMenuItemCarregarDadosActionPerformed

