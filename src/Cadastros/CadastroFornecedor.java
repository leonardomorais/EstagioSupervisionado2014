    private void jBtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGravarActionPerformed
        if (jTextFieldNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome do fornecedor é obrigatório!");
            jTextFieldNome.grabFocus();
        } 
        else if (jFormattedTextFieldCpfCnpj.getText().equals("   .   .   -  ")||
                jFormattedTextFieldCpfCnpj.getText().equals("  .   .   /    -  ")) {
            JOptionPane.showMessageDialog(null, "O CPF/CNPJ do fornecedor é obrigatório!");
            jFormattedTextFieldCpfCnpj.grabFocus();
        } 
        else if (rotina == Rotinas.incluir && !doc.validaCPFCNPJ(jFormattedTextFieldCpfCnpj.getText())){
            JOptionPane.showMessageDialog(null, "CPF/CNPJ inválido!");
            jFormattedTextFieldCpfCnpj.grabFocus();
        }
        else if (rotina == Rotinas.alterar && jRadioButtonPfisica.isSelected() 
                && !doc.validaCPF(doc.removeMascara(jFormattedTextFieldCpfCnpj.getText()))){
            JOptionPane.showMessageDialog(null, "CPF inválido!");
            jFormattedTextFieldCpfCnpj.grabFocus();
        }
        else if (rotina == Rotinas.alterar && jRadioButtonPJuridica.isSelected()
                && !doc.validaCNPJ(doc.removeMascara(jFormattedTextFieldCpfCnpj.getText()))){
            JOptionPane.showMessageDialog(null, "CNPJ inválido!");
            jFormattedTextFieldCpfCnpj.grabFocus();
        }
        else {
            carregarFornecedor();
            carregarEndereco();
            carregarContato();

            if (rotina == Rotinas.incluir) {
                fornecedor.incluir(fornecedor);
                JOptionPane.showMessageDialog(null, "Fornecedor gravado com sucesso!");
                jTextFieldCdFornecedor.setText(fornecedor.getCdFornecedor().toString());
            } else if (rotina == Rotinas.alterar) {
                if (jTextFieldCdFornecedor.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "É preciso informar o código do fornecedor que deseja alterar!");
                    jTextFieldCdFornecedor.grabFocus();
                } else {
                    try {
                        fornecedor.setCdFornecedor(Integer.parseInt(jTextFieldCdFornecedor.getText()));
                        fornecedor.alterar(fornecedor);
                        JOptionPane.showMessageDialog(null, "Fornecedor alterado com sucesso!");
                    } catch (NumberFormatException ex) {

                    }
                }
            }
            rotina = Rotinas.padrao;
            botoes.validaBotoes(jPanelBotoes, rotina);
        }
    }//GEN-LAST:event_jBtGravarActionPerformed

    private void jTextFieldCdFornecedorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCdFornecedorFocusLost
        if (!jTextFieldCdFornecedor.getText().equals("")) {
            try {
                fornecedor.setCdFornecedor(Integer.parseInt(jTextFieldCdFornecedor.getText()));
                fornecedor.retornaFornecedor(fornecedor);

                if (fornecedor.getPessoa().getNome().equals("")) {
                    jBtCancelarActionPerformed(null);
                } else {
                    limpar.limparCampos(jPanelCadastro);
                    preencherCampos();
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Por favor informe um código!");
                jBtCancelarActionPerformed(null);
            }
        }
    }//GEN-LAST:event_jTextFieldCdFornecedorFocusLost

    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        PreencherTabela preencher = new PreencherTabela();
        preencher.FormatarJtable(jTableConsulta, new int[]{40, 180, 45, 100, 120, 180, 60, 40});
        
        switch (jComboBoxConsulta.getSelectedIndex()){
            case 0:
                preencher.PreencherJtableGenerico(jTableConsulta, fornecedor.consultarGeral(true));
                report.setConsulta(preencher.getConsulta());
                editaBotao(preencher.Vazia());
            break;
                
            case 1:
                try{
                    fornecedor.setCdFornecedor(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableConsulta, fornecedor.consultarCdFornecedor(fornecedor, true));
                    report.setConsulta(preencher.getConsulta());
                    editaBotao(preencher.Vazia());
                }
                catch(NumberFormatException ex){
                    JOptionPane.showMessageDialog(null, "Por favor informe um código para pesquisar!");
                    editaBotao(true);
                }
            break;
                
            default:
                fornecedor.getPessoa().setNome(jTextFieldConsulta.getText().toUpperCase());
                preencher.PreencherJtableGenerico(jTableConsulta, fornecedor.consultarNomeFornecedor(fornecedor, true));
                report.setConsulta(preencher.getConsulta());
                editaBotao(preencher.Vazia());
            break;    
        }
    }//GEN-LAST:event_jBtPesquisarActionPerformed

    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        if (jTextFieldCdFornecedor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "É preciso informar o código do fornecedor que deseja excluir!");
            jTextFieldCdFornecedor.grabFocus();
        } else {
            try {
                fornecedor.setCdFornecedor(Integer.parseInt(jTextFieldCdFornecedor.getText()));

                int delete = JOptionPane.showConfirmDialog(null, "Deseja realmente inativar este fornecedor ?",
                        "Inativar Fornecedor", JOptionPane.YES_NO_OPTION);

                if (delete == JOptionPane.YES_OPTION) {
                    fornecedor.excluir(fornecedor);
                    JOptionPane.showMessageDialog(null, "Fornecedor inativado com sucesso!");
                    limpar.limparCampos(jPanelCadastro);
                }
            } catch (NumberFormatException ex) {

            }
        }
        rotina = Rotinas.excluir;
        botoes.validaBotoes(jPanelBotoes, rotina);
    }//GEN-LAST:event_jBtExcluirActionPerformed

