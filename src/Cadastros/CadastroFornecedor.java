    public void carregarFornecedor() {
        fornecedor.getPessoa().setNome(jTextFieldNome.getText().toUpperCase());

        fornecedor.setDtCadastro(jFormattedTextFieldDataCadastro.getText());

        if (jComboBoxSituacao.getSelectedIndex() == 0) {
            fornecedor.setInAtivo("A");
        } else {
            fornecedor.setInAtivo("I");
        }
        if (jRadioButtonPfisica.isSelected()) {
            carregarFornecedorFisico();
        } else {
            carregarFornecedorJuridico();
        }
        fornecedor.getPessoa().setDtCadastro(fornecedor.getDtCadastro());
        fornecedor.getPessoa().setInAtivo(fornecedor.getInAtivo());
    }

    public void carregarFornecedorFisico() {
        fornecedor.getPessoa().setTpPessoa("F");
        fornecedor.getPessoa().getPfisica().setCPF(jFormattedTextFieldCpfCnpj.getText());
        fornecedor.getPessoa().getPfisica().setRG(jTextFieldRG.getText());

        if (jComboBoxSexo.getSelectedIndex() == 0) {
            fornecedor.getPessoa().getPfisica().setSexo("M");
        } else {
            fornecedor.getPessoa().getPfisica().setSexo("F");
        }
    }

    public void carregarFornecedorJuridico() {
        fornecedor.getPessoa().setTpPessoa("J");
        fornecedor.getPessoa().getPjuridica().setCNPJ(jFormattedTextFieldCpfCnpj.getText());
        fornecedor.getPessoa().getPjuridica().setRazao(jTextFieldRazao.getText());
    }

    public void carregarEndereco() {
        fornecedor.getPessoa().getEndereco().getCidade().setCdCidade(jComboBoxCidade.getSelectedIndex());
        fornecedor.getPessoa().getEndereco().setRuaAv(jTextFieldRua.getText().toUpperCase());
        fornecedor.getPessoa().getEndereco().setNrEndereco(jTextFieldNrEndereco.getText());
        fornecedor.getPessoa().getEndereco().setNrSequencia(1);
        fornecedor.getPessoa().getEndereco().setCEP(jTextFieldCEP.getText());
        fornecedor.getPessoa().getEndereco().setBairro(jTextFieldBairro.getText().toUpperCase());
        fornecedor.getPessoa().getEndereco().setComplemento(jTextFieldComplemento.getText().toUpperCase());
    }

    public void carregarContato() {
        fornecedor.getPessoa().getContato().setEmail(jTextFieldEmail.getText().toUpperCase());
        fornecedor.getPessoa().getContato().setNrSeq(1);
        fornecedor.getPessoa().getContato().setNomeContato(jTextFieldNome.getText().toUpperCase());
        preencheFones();
    }

    public void preencherCampos() {
        jTextFieldCdFornecedor.setText(fornecedor.getCdFornecedor().toString());
        jTextFieldNome.setText(fornecedor.getPessoa().getNome());
        jFormattedTextFieldDataCadastro.setText(fornecedor.getDtCadastro());
        jComboBoxSituacao.setSelectedItem(fornecedor.getInAtivo());

        jComboBoxCidade.setSelectedItem(fornecedor.getPessoa().getEndereco().getCidade().getDsCidade());

        jTextFieldCEP.setText(fornecedor.getPessoa().getEndereco().getCEP());
        jTextFieldNrEndereco.setText(fornecedor.getPessoa().getEndereco().getNrEndereco());
        jTextFieldRua.setText(fornecedor.getPessoa().getEndereco().getRuaAv());
        jTextFieldBairro.setText(fornecedor.getPessoa().getEndereco().getBairro());
        jTextFieldComplemento.setText(fornecedor.getPessoa().getEndereco().getComplemento());

        jTextFieldEmail.setText(fornecedor.getPessoa().getContato().getEmail());
        jTextFieldFoneRes.setText(fornecedor.getPessoa().getContato().getFoneRes());
        jTextFieldFoneCom.setText(fornecedor.getPessoa().getContato().getFoneCom());
        jTextFieldFoneCel.setText(fornecedor.getPessoa().getContato().getFoneCel());

        if (fornecedor.getPessoa().getTpPessoa().equals("F")) {
            jRadioButtonPfisica.setSelected(true);
            jRadioButtonPfisicaActionPerformed(null);
            jComboBoxSexo.setSelectedItem(fornecedor.getPessoa().getPfisica().getSexo());
            jFormattedTextFieldCpfCnpj.setText(fornecedor.getPessoa().getPfisica().getCPF());
            jTextFieldRG.setText(fornecedor.getPessoa().getPfisica().getRG());
        } else {
            jRadioButtonPJuridica.setSelected(true);
            jRadioButtonPJuridicaActionPerformed(null);
            jFormattedTextFieldCpfCnpj.setText(fornecedor.getPessoa().getPjuridica().getCNPJ());
            jTextFieldRazao.setText(fornecedor.getPessoa().getPjuridica().getRazao());
        }
    }

    public void preencheFones() {
        if (jTextFieldFoneRes.getText().equals("(  )-    -    ")) {
            fornecedor.getPessoa().getContato().setFoneRes("");
        } else {
            fornecedor.getPessoa().getContato().setFoneRes(jTextFieldFoneRes.getText());
        }

        if (jTextFieldFoneCom.getText().equals("(  )-    -    ")) {
            fornecedor.getPessoa().getContato().setFoneCom("");
        } else {
            fornecedor.getPessoa().getContato().setFoneCom(jTextFieldFoneCom.getText());
        }

        if (jTextFieldFoneCel.getText().equals("(  )-    -    ")) {
            fornecedor.getPessoa().getContato().setFoneCel("");
        } else {
            fornecedor.getPessoa().getContato().setFoneCel(jTextFieldFoneCel.getText());
        }
    }

    public void editaBotao(boolean vazia) {
        if (vazia) {
            jBtRelatorio.setEnabled(false);
            jTextFieldConsulta.setText("");
            jTextFieldConsulta.grabFocus();
        } else {
            jBtRelatorio.setEnabled(true);
        }
    }
