    private void jBtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtGravarActionPerformed
        String campoData = data.getMask().replace("#", " ");
        if (jTextFieldNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome do funcionário é obrigatório!");
            jTextFieldNome.grabFocus();
        } else if (jTextFieldCPF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O CPF do funcionário é obrigatório!");
            jTextFieldCPF.grabFocus();
        } else if (jFormattedTextFieldData.getText().equals(campoData)) {
            JOptionPane.showMessageDialog(null, "Por favor informe a data de cadastro!");
            jFormattedTextFieldData.grabFocus();
        } else if (!doc.validaCPFCNPJ(jTextFieldCPF.getText())){
            JOptionPane.showMessageDialog(null, "CPF/CNPJ inválido!");
            jTextFieldCPF.grabFocus(); 
        }
        else {
            carregarFuncionario();
            carregarContato();
            carregarEndereco();

            if (rotina == Rotinas.incluir) {
                funcionario.incluir(funcionario);
                JOptionPane.showMessageDialog(null, "Funcionário gravado com sucesso!");
                jTextFieldCdFuncionario.setText(funcionario.getCd_funcionario().toString());
            } else if (rotina == Rotinas.alterar) {
                if (jTextFieldCdFuncionario.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "É preciso informar o código do funcionário que deseja alterar!");
                    jTextFieldCdFuncionario.grabFocus();
                } else {
                    try {
                        funcionario.setCd_funcionario(Integer.parseInt(jTextFieldCdFuncionario.getText()));
                        funcionario.alterar(funcionario);
                        JOptionPane.showMessageDialog(null, "Funcionário alterado com sucesso!");
                    } catch (NumberFormatException ex) {
                    }
                }
            }
            rotina = Rotinas.padrao;
            botoes.validaBotoes(jPanelBotoes, rotina);
        }
    }//GEN-LAST:event_jBtGravarActionPerformed

    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        if (jTextFieldCdFuncionario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "É preciso informar o código do funcionário que deseja excluir!");
            jTextFieldCdFuncionario.grabFocus();
        } else {
            try {
                funcionario.setCd_funcionario(Integer.parseInt(jTextFieldCdFuncionario.getText()));

                int delete = JOptionPane.showConfirmDialog(null, "Deseja realmente inativar este funcionário ?",
                        "Inativar Funcionário", JOptionPane.YES_NO_OPTION);

                if (delete == JOptionPane.YES_OPTION) {
                    funcionario.excluir(funcionario);
                    JOptionPane.showMessageDialog(null, "Funcionário inativado com sucesso!");
                    limpar.limparCampos(jPanelCadastro);
                }
            } catch (NumberFormatException ex) {
            }
        }
        rotina = Rotinas.excluir;
        botoes.validaBotoes(jPanelBotoes, rotina);
    }//GEN-LAST:event_jBtExcluirActionPerformed

