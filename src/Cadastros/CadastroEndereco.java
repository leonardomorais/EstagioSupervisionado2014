    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        PreencherTabela preencher = new PreencherTabela();

        preencher.FormatarJtable(jTableConsulta, new int[]{40, 40, 200, 100, 120, 40, 80, 60, 55});

        switch (jComboBoxConsulta.getSelectedIndex()){
            case 0:
                preencher.PreencherJtableGenerico(jTableConsulta, endereco.consultarGeral());
            break;
            
            case 1:
                try {
                    endereco.setCdPessoa(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableConsulta, endereco.consultarCodigo(endereco));
                } 
                catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Por favor informe um código!");
                    jTextFieldConsulta.setText("");
                    jTextFieldConsulta.grabFocus();
                }
            break;
                
            default:
                pessoa.setNome(jTextFieldConsulta.getText().toUpperCase());
                preencher.PreencherJtableGenerico(jTableConsulta, endereco.consultarNome(pessoa));
            break;    
        }
        if (preencher.Vazia()){
            JOptionPane.showMessageDialog(null, "A consulta não encontrou resultados!");
            jTextFieldConsulta.setText("");
        }
    }//GEN-LAST:event_jBtPesquisarActionPerformed

    private void jMenuItemCarregaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemCarregaActionPerformed
        int linha = jTableConsulta.getSelectedRow();

        if (linha >= 0) {
            int cd = Integer.parseInt(jTableConsulta.getValueAt(linha, 0).toString());
            int nr = Integer.parseInt(jTableConsulta.getValueAt(linha, 1).toString());
            endereco.setCdPessoa(cd);
            endereco.setNrSequencia(nr);

            endereco.retornaEndereco(endereco);
            pessoa.setCdPessoa(cd);
            pessoa.retornaPessoa(pessoa);

            preencherCampos();
            jTabbedPaneEndereco.setSelectedIndex(0);

            rotina = Rotinas.padrao;
            botoes.validaBotoes(jPanelBotoes, rotina);
        }
    }//GEN-LAST:event_jMenuItemCarregaActionPerformed

    private void jBtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtExcluirActionPerformed
        // Aqui todos os campos estão vazios
        if (jTextFieldCdPessoa.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "É preciso informar o código da pessoa que deseja excluir os endereços!");
            jTextFieldCdPessoa.grabFocus();
        } // Aqui o Código da pessoa está preenchido mas o de endereço está vazio
        else if (jTextFieldNrSeq.getText().equals("")) {
            try {
                endereco.setCdPessoa(Integer.parseInt(jTextFieldCdPessoa.getText()));

                int opcao = JOptionPane.showConfirmDialog(null, "Não foi selecionado nenhum código de endereço.\nDeseja "
                        + "excluir todos os endereços desta pessoa ?", "Excluir Endereços", JOptionPane.YES_NO_OPTION);
                if (opcao == JOptionPane.YES_OPTION) {
                    int delete = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir todos os endereços da pessoa ?",
                            "Excluir Todos os Endereços", JOptionPane.YES_NO_OPTION);
                    if (delete == JOptionPane.YES_OPTION) {
                        endereco.excluirTodos(endereco);
                        JOptionPane.showMessageDialog(null, "Endereços excluídos com sucesso!");
                        limpar.limparCampos(jPanelCadastro);
                    }
                } else {
                    jTextFieldNrSeq.grabFocus();
                }
            } catch (NumberFormatException ex) {
            }

        } // Aqui ambos os códigos estão preenchidos
        else {
            try {
                endereco.setCdPessoa(Integer.parseInt(jTextFieldCdPessoa.getText()));
                endereco.setNrSequencia(Integer.parseInt(jTextFieldNrSeq.getText()));

                int excluir = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir este endereço",
                        "Excluir Endereço", JOptionPane.YES_NO_OPTION);

                if (excluir == JOptionPane.YES_OPTION) {
                    endereco.excluir(endereco);
                    JOptionPane.showMessageDialog(null, "Endereço excluído com sucesso!");
                    limpar.limparCampos(jPanelCadastro);
                }
            } catch (NumberFormatException ex) {
            }
        }
        rotina = Rotinas.excluir;
        botoes.validaBotoes(jPanelBotoes, rotina);

    }//GEN-LAST:event_jBtExcluirActionPerformed

