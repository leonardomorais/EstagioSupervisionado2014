    private void preencherTabela(){
        Telas telas = new Telas();
        int linha = jTableNivel.getSelectedRow();
        if (linha >= 0) {
            int cd = Integer.parseInt(jTableNivel.getValueAt(linha, 0).toString());
            
            PreencherTabela preencher = new PreencherTabela();
            preencher.FormatarJtable(jTableTelas, new int [] {60, 200, 60, 60, 60, 60, 60});
            preencher.PreencherJtableGenerico(jTableTelas, telas.consultarTelasNivel(cd));
        }
    }
