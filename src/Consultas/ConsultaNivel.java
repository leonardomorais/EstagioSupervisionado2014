    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        PreencherTabela preencher = new PreencherTabela();
        preencher.FormatarJtable(jTableNivel, new int [] {100, 360, 100});
        
        switch (jComboBoxConsulta.getSelectedIndex()){
            case 0:
                preencher.PreencherJtableGenerico(jTableNivel, nivel.consultarGeral(false));
            break;
                
            case 1:
                try{
                    nivel.setCdNivel(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableNivel, nivel.consultarCdNivel(nivel, false));
                }
                catch(NumberFormatException ex){
                    JOptionPane.showMessageDialog(null, "Por favor informe um código para pesquisar!");
                    jTextFieldConsulta.setText("");
                    jTextFieldConsulta.grabFocus();
                }
            break;
                
            default:
                nivel.setDsNivel(jTextFieldConsulta.getText().toUpperCase());
                preencher.PreencherJtableGenerico(jTableNivel, nivel.consultarDescricao(nivel, false));
            break;    
        }
    }//GEN-LAST:event_jBtPesquisarActionPerformed

    private void jTableNivelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNivelMouseClicked
        int linha = jTableNivel.getSelectedRow(); 
        nivel.setCdNivel(Integer.parseInt(jTableNivel.getValueAt(linha, 0).toString()));
        nivel.retornaNivel(nivel, true);
        preencherTabela();
    }//GEN-LAST:event_jTableNivelMouseClicked

    private void jBtSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSelecionarActionPerformed
        int linha = jTableNivel.getSelectedRow();
        if (linha >= 0) {
                dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro !");
        }
    }//GEN-LAST:event_jBtSelecionarActionPerformed

    private void jBtCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtCancelarActionPerformed
        nivel.setCdNivel(0);
        dispose();
    }//GEN-LAST:event_jBtCancelarActionPerformed

    private void jTableNivelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableNivelKeyPressed
        int linha = jTableNivel.getSelectedRow();
            if (linha >= 0 && evt.getKeyChar() == KeyEvent.VK_ENTER) {
                nivel.setCdNivel(Integer.parseInt(jTableNivel.getValueAt(linha, 0).toString()));
                nivel.retornaNivel(nivel, true);
                jBtSelecionarActionPerformed(null);
            }
    }//GEN-LAST:event_jTableNivelKeyPressed

