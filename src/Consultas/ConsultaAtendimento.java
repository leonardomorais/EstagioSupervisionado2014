    private void jBtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtPesquisarActionPerformed
        PreencherTabela preencher = new PreencherTabela();
        preencher.FormatarJtable(jTableAtendimentos, new int[]{70, 50, 70, 195, 70, 70, 70, 60, 70});

        switch (jComboBoxConsulta.getSelectedIndex()) {
            case 0:
                // Consultar Geral
                preencher.PreencherJtableGenerico(jTableAtendimentos,
                        venda.getVendaAtendimento().getAtendimento().consultarGeral(true));
                break;
            case 1:
                // Consultar por Nr de Atendimento
                try {
                    venda.getVendaAtendimento().getAtendimento().
                            setNrAtendimento(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableAtendimentos,
                            venda.getVendaAtendimento().getAtendimento().
                            consultarNrAtendimento(venda.getVendaAtendimento().getAtendimento(), true));
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Informe um número de atendimento para pesquisar");
                    jTextFieldConsulta.setText("");
                    jTextFieldConsulta.grabFocus();
                }
                break;
            case 2:
                // Consultar por Nr da Mesa
                try {
                    venda.getVendaAtendimento().getAtendimento().getMesa().
                            setNrMesa(Integer.parseInt(jTextFieldConsulta.getText()));
                    preencher.PreencherJtableGenerico(jTableAtendimentos,
                            venda.getVendaAtendimento().getAtendimento()
                            .consultarNrMesa(venda.getVendaAtendimento().getAtendimento(), true));
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Informe um número de mesa para pesquisar");
                    jTextFieldConsulta.setText("");
                    jTextFieldConsulta.grabFocus();
                }

                break;

            case 3:
                // Consultar por nome do funcionário
                venda.getVendaAtendimento().getAtendimento().getFuncionario()
                        .getPessoa().setNome(jTextFieldConsulta.getText().toUpperCase());
                preencher.PreencherJtableGenerico(jTableAtendimentos,
                        venda.getVendaAtendimento().getAtendimento().
                        consultarNomeFuncionario(venda.getVendaAtendimento().getAtendimento(), true));
                break;

            default:
                // Consultar por data
                break;
        }
    }//GEN-LAST:event_jBtPesquisarActionPerformed

    private void jBtSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtSelecionarActionPerformed
        int linha = jTableAtendimentos.getSelectedRow();
        if (linha >= 0) {
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Por favor selecione um registro !");
        }
    }//GEN-LAST:event_jBtSelecionarActionPerformed

    private void jTableAtendimentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAtendimentosMouseClicked
        int linha = jTableAtendimentos.getSelectedRow();
        venda.getVendaAtendimento().getAtendimento()
                .setNrAtendimento(Integer.parseInt(jTableAtendimentos.getValueAt(linha, 0).toString()));
        
        venda.getVendaAtendimento().getAtendimento().setVlTotal(
                Double.parseDouble(jTableAtendimentos.getValueAt(linha, 8).toString()));
        //
        PreencherTabela tabela = new PreencherTabela();
        if (linha >= 0) {
            venda.getVendaAtendimento().getAtendimento().setNrAtendimento(Integer.parseInt(jTableAtendimentos.getValueAt(linha, 0).toString()));
            venda.getVendaAtendimento().getAtendimento().getAtdProdutos().setNrAtendimento(venda.getVendaAtendimento().getAtendimento().getNrAtendimento());
            tabela.FormatarJtable(jTableAtendimentoProdutos, new int[]{100, 100, 175, 100, 100, 100});
            tabela.PreencherJtableGenerico(jTableAtendimentoProdutos,
                    venda.getVendaAtendimento().getAtendimento().getAtdProdutos()
                    .consultarProdutos(venda.getVendaAtendimento().getAtendimento().getAtdProdutos()));
        }

    }//GEN-LAST:event_jTableAtendimentosMouseClicked

